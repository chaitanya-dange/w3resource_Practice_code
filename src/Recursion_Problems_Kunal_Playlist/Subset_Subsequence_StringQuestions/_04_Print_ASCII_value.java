package Recursion_Problems_Kunal_Playlist.Subset_Subsequence_StringQuestions;

public class _04_Print_ASCII_value {
    public static void main(String[] args) {
        System.out.println('a'-0); // giving ascii value
        System.out.println('a'+0); // giving the ascii value
        System.out.println('a'-'0'); //subtracting ascii value of 0 which is 48 and ascii value of a that is  97
        System.out.println('5'-'0');//good for number
        // converting character which are int to int by subtracting char of '0'
    }
}
